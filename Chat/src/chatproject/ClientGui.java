    /**
     *Connect the client when the client press the connect button 
     * @param evt 
     */
    private void ConnectBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ConnectBtnActionPerformed
        username = NameField.getText().trim();
        if (!isConnected) {
            client = new ClientThread(username, AddressField.getText().trim());
            client.start();
        } else {
            client.disconnect();
        }
    }//GEN-LAST:event_ConnectBtnActionPerformed

    /**
     * Send the message if the send button pressed.
     * @param evt 
     */
    private void SendBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SendBtnActionPerformed
        client.SendMessage(MsgField.getText().trim());
        MsgField.setText("");
    }//GEN-LAST:event_SendBtnActionPerformed

    /**
     * Disconnect the client from the chat if the windows is closed. 
     * @param evt 
     */
    private void formWindowClosing(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowClosing
        if (isConnected) {
            client.disconnect();
        }
    }//GEN-LAST:event_formWindowClosing

    /**
     * Online users list,the client pressed on name in the list. 
     * @param evt 
     */
    private void usersListValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_usersListValueChanged
        if (usersList.getSelectedValue() != null) {
            switch (usersList.getSelectedValue()) {
                case "Lobby":
                    MsgField.setText("");
                    MsgField.requestFocus();
                    break;
                default:
                    MsgField.setText("<set_msg><" + usersList.getSelectedValue() + ">");
                    MsgField.requestFocus();
                    break;
            }
        }
    }//GEN-LAST:event_usersListValueChanged
    
