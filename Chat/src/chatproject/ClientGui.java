    /**
     * Send messsage when the user press ENTER key
     * @param evt 
     */
    private void MsgFieldKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_MsgFieldKeyReleased
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            client.SendMessage(MsgField.getText().trim());
            MsgField.setText("");
        }
    }//GEN-LAST:event_MsgFieldKeyReleased

